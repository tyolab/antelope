	#include <stdio.h>
	#include <ctype.h>

	int main(void)
	{
	int ch;
	const unsigned int base = 27;
	unsigned int val = 0;

	printf("unsigned char ANT_header_hash_encode_27[] = \n{\n");
	for (ch = 0; ch <= 0xFF; ch++)
		{
		if (ch % 0x10 == 0 && ch != 0)
			printf("\n");

		if (ch >= 'a' && ch <= 'z')
			printf("0x%02X/*%c*/", 1 + ch - 'a', ch);	// assign a-z to the values 0x01-0x1A
		else if (ch >= 'A' && ch <= 'Z')
			printf("0x%02X/*%c*/", 1 + ch - 'A', ch);	// assign A-Z to the values 0x01-0x1A
		else if (ch == '_' || ch == '~' || ch == '@' || ch == '-')
			printf("0x%02X/*%c*/", 0, ch);				// assign ~,_,-,@ to 0x00
		else
			{											// distribute the others evenly throughout the number range
			val = (val + 1) % base;
			printf("0x%02X/*%c*/", val, isprint(ch) ? ch : '.');
			}

		if (ch != 0xFF)
			printf(", ");
		}
	printf("\n};\n");

	return 0;
	}

